package Models;

/**
 *
 * @author win
 */
public class ProductSize {
    private int id;
    private int pid;
    private String size;
    private int quantity;

    public ProductSize() {
    }

    public ProductSize(int id, int pid, String size, int quantity) {
        this.id = id;
        this.pid = pid;
        this.size = size;
        this.quantity = quantity;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
    
    
}
